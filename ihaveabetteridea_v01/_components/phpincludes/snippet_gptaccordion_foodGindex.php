			<p>Although  there are food items known mainly as carbohydrates or protein or fatty foods, you  can also get carbohydrate, protein and fat from foods like dairy products,  nuts, lentils, pulses and some vegetables; which can be identified as one or  the other (carbohydrate or protein or fatty food).&nbsp; You therefore need to be careful how you mix  protein with carbohydrates.</p>
			<p>Simply put,  GI (Glycemic Index) is an index given to food items containing carbohydrate, giving it a ranking according to its effect on blood glucose levels.&nbsp; Using this index, you can make up your meals  more effectively to prevent yourself from having too much of one food group and  not enough of the other.&nbsp; Food items with GI of 55 or less are ranked as low, 56-69 as medium and 70 or more is high.  (The GI of some food items can be determined by searching through the <a href="http://www.glycemicindex.com/" title="GI database" target="_blank">GI database</a>.)&nbsp; In general,  use the guidelines below when making up your meals according to their GI.</p>
			<ul>
				<li>Carbohydrates&ndash;to&ndash;protein ratio in  your food should be approximately 2&ndash;to&ndash;1 (2&ndash;1 grams).&nbsp; 70&ndash;80% of the total carbohydrate portion of  each meal should come from foods that have GI below 80. &nbsp;For carbs with high GI, the ratio should be 1&ndash;to&ndash;1. </li>
				<li>Try to choose at least one low GI  food at each meal.&nbsp; If you need to, combine  high GI food, with a low GI food [e.g. 1/2 a bagel (high GI) with a bowl of salad  (low GI), or corn flakes cereal (high GI) topped with a spoonful of All Bran  (low GI) and some fruits (low GI)].</li>
				<li>Reduce the amount of potatoes you  eat &ndash; they are very high in GI.</li>
				<li>Avoid processed, refined starchy  foods (they are low in fibre and other nutrients and have a higher GI).</li>
				<li>Use vinaigrette salad dressing instead  of a creamy salad dressing &ndash; it is lower in fat, plus the acidity of the vinegar  slows digestion, increasing the meal&rsquo;s GI.</li>
				<li>Watch your <a href="http://www.nhs.uk/Livewell/Goodfood/Pages/eatwell-plate.aspx" title="Portion plate" target="_blank">portion sizes</a> &ndash; the  bigger the portion, the more it will increase your blood glucose and the  overall GI of the meal, regardless of the food item&rsquo;s GI.</li>
				<li>Wait at least 30 minutes before or  after a meal before eating a snack (e.g. fruit, low GI nuts etc)</li>
			</ul>
