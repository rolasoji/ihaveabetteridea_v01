			<p>General preparation for each day is the key to a good diet and exercise plan.  You need to prepare for your meals and snacks for the day.  You should also work out the best time  you can exercise and take advantage of opportunities  you can use to add to your activities  (e.g. walking to work or from the bus / train station to work, taking a walk at lunch times etc.)  The following may also help:</p>
			<ul>
				<li>Plan your meals ahead of time each day.  This will help you to avoid missing meals and perhaps overeating when next you eat. If you do miss meals, try not to give in to the urge to eat too much until you stop feeling hungry. Instead, stick to your normal portion and wait for about half an hour, then eat some nuts or fruit if you still need to. It takes a while for the body to recover after adjusting to lack of food and though you may be full, you may not feel full for quite a while, and consequently overeat. </li>
				<li>As much as possible, make your own meals and avoid processed meals.  Prepare some meal items and keep them refrigerated or go for meals you can prepare quickly.</li>
				<li>Avoid prepared smoothie, salads or sandwiches etc., and prepare your own.  Most of the store-bought ones have the wrong proportions of the food groups (protein and vegetables) and smoothies may be sweetened or may contain preservatives.</li>
