    <h2>Good practice tips</h2>
    <p>The first step is to find a diet and exercise plan that will help you to keep your weight under control.  Aim to find 1 that'll work for you long-term (perhaps forever) instead of quick-fix solutions.  In other words, a plan that'll help you need to change old habits.  If you do it right, you should lose the excess weight and in the process develop new habits that may help keep your weight and body healthy.  The following are some good practice I found useful &ndash; collated from different sources.</p>
